    private void actualizarTabla(){
        String[][] datos = new String[b1.getIdC() + b1.getIdF()][8];
        for (int i = 0; i < b1.getIdC(); i++) {
            datos[i][0] = b1.getDigimones().get(i).getId() + "";
            datos[i][1] = "canino";
            datos[i][2] = b1.getDigimones().get(i).getNombre();
            datos[i][3] = b1.getDigimones().get(i).getRaza();
            datos[i][4] = b1.getDigimones().get(i).getColor();
            datos[i][5] = b1.getDigimones().get(i).getEdad() + "";
            datos[i][6] = b1.getDigimones().get(i).getNivelE() + "";
            datos[i][7] = "no aplica";
        }
        for (int i = 0; i < b1.getIdF(); i++) {
            datos[i + b1.getIdC()][0] = b1.getPokemones().get(i).getId() + "";
            datos[i + b1.getIdC()][1] = "felino";
            datos[i + b1.getIdC()][2] = b1.getPokemones().get(i).getNombre();
            datos[i + b1.getIdC()][3] = b1.getPokemones().get(i).getRaza();
            datos[i + b1.getIdC()][4] = b1.getPokemones().get(i).getColor();
            datos[i + b1.getIdC()][5] = b1.getPokemones().get(i).getEdad() + "";
            datos[i + b1.getIdC()][6] = "no aplica";
            datos[i + b1.getIdC()][7] = b1.getPokemones().get(i).isToxo() + "";
        }
        tablaDatos.setModel(new javax.swing.table.DefaultTableModel(
            datos,
            new String [] {
            "Id", "Tipo", "Nombre", "Raza", "Color", "Edad", "Entrenamiento", "Toxoplasmosis"
        }));
    }
